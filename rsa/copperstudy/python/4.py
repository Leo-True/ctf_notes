from sage.all import *

def are_all_coprime(numbers):
    count = len(numbers)
    for i in range(count):
        for j in range(i+1, count):
            if not gcd(numbers[i], numbers[j]) == 1:
                print(f"Not coprime: [{i}] - [{j}]")
                return False
    return True


